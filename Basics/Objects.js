// Objects in Js are like dictionaries in python
const my_dict = {"name":"Prince", "age":20, "Cgpa":10};
console.log(my_dict.name);  //Accessing Values
console.log(my_dict["name"]);
// We can also Change Values
my_dict["age"] = 22;
console.log(my_dict["age"]);
// Since Object is created using const, we cannot change each value simultanously.
